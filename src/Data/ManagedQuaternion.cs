namespace NEP.MonoDirector.Data
{
    public class ManagedQuaternion
    {
        public ManagedQuaternion(float x, float y, float z, float w)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }

        public float x;
        public float y;
        public float z;
        public float w;
    }
}
